# A list in Python is an ordered, mutable (changeable) collection of items. Lists can contain elements of any data type, and items can be repeated.
friends=["Apple","Banana","Cherry",1,12.4,True]
print(friends)
print(friends[0])
print(friends[-1])
print(friends[0:2]) # starts from index 0 and ends at index 
print(friends[0])
friends[0]="Peach"
print(friends[0])
#  list are mutable unlike strings